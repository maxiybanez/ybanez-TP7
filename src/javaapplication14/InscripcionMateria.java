    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        // TODO add your handling code here:
        
        Materia materia= (Materia) jcMaterias.getSelectedItem();
        Alumno alumno= (Alumno) jcAlumno.getSelectedItem();
        
        if(!alumno.contieneMateria(materia)){
            alumno.agregarMaterias(materia);
            JOptionPane.showMessageDialog(this, "Inscripcion completa");
        }else{
            JOptionPane.showMessageDialog(this, "Ya esta inscripto en la materia");   
        }
        
        
        
    }//GEN-LAST:event_jbInscribirActionPerformed

    private void llenarComboAlumno(){
          HashSet <Alumno> alumnos = Menu.getAlumnos();
          for(Alumno alumno:alumnos){
                   jcAlumno.addItem(alumno);
          }
    }
    private void llenarComboMaterias(){
          HashSet<Materia> materias= Menu.getMaterias();
          for(Materia materia:materias){
                   jcMaterias.addItem(materia);
          }
    }

